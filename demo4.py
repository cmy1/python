# 整数类型
# 可以表示 正数 负数 0
n1 = 90
n2 = -70
n3 = 0
print(n1, type(n1))
print(n2, type(n2))
print(n3, type(n3))
# 整数可以表示为二进制，十进制，八进制，十六进制
print('十进制', 118)
print('二进制', 0b10101111)   # 二进制以0b开头
print('八进制', 0o176)        # 八进制以0o开头
print('十六进制', 0x1EAF)
